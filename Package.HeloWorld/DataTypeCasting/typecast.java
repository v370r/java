package DataTypeCasting;

public class typecast {
    public static void main(String[] args) {
        double a = 50.05;
        System.out.println("double data type "+ a);

        float f = (float)a;
        System.out.println("float data type "+ f );

        int i = (int)a;
        System.out.println("int data type " + i);
    }
}
